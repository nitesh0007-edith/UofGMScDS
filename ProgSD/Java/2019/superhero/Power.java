package superhero;

/**
 * Power enum - superhero abilities/powers
 *
 * EXAM PATTERN: Simple enum with many values
 * TIME: 2 minutes
 * MARKS: 2
 *
 * Key concepts:
 * - Enum for fixed set of values
 * - Superhero game powers
 * - Used in Set to track multiple powers per character
 */
public enum Power {
    // Physical powers
    FLIGHT,
    SPEED,
    STRENGTH,
    SMALL,           // Size manipulation (shrinking)
    INVINCIBILITY,
    WATER,           // Water-based abilities

    // Mental/Special powers
    TELEPATHY,
    MAGIC,
    TRANSFORMATION,  // Shape-shifting

    // Technology/Science
    COMPUTER,
    SCIENCE,
    WEAPONS,

    // Energy powers
    ENERGY_BLAST,
    MAGNETISM,

    // Clone powers
    CLONING
}
